CREATE TABLE IF NOT EXISTS Newsletter(
    id INT PRIMARY KEY AUTO_INCREMENT,
    emails VARCHAR(255) NOT NULL UNIQUE,
    records VARCHAR(255) NOT NULL
);

CREATE TABLE IF NOT EXISTS Contact(
    id INT PRIMARY KEY AUTO_INCREMENT,
    names VARCHAR(255) NOT NULL,
    subjects VARCHAR(255) NOT NULL,
    emails VARCHAR(255) NOT NULL,
    phones VARCHAR(255) NOT NULL,
    messages VARCHAR(255) NOT NULL
);

CREATE TABLE IF NOT EXISTS Register(
    id INT PRIMARY KEY AUTO_INCREMENT,
    fname VARCHAR(255) NOT NULL,
    lname VARCHAR(255) NOT NULL,
    emails VARCHAR(255) NOT NULL UNIQUE,
    passwords VARCHAR(255) NOT NULL,
    phones VARCHAR(255) NOT NULL UNIQUE,
    country VARCHAR(255) NOT NULL,
    states VARCHAR(255) NOT NULL,
    faddress VARCHAR(255) NOT NULL,
    laddress VARCHAR(255) NOT NULL,
    postal VARCHAR(255) NOT NULL,
    company VARCHAR(255) NOT NULL,
    cardnames VARCHAR(255) NOT NULL,
    cardnumbers VARCHAR(255) NOT NULL,
    expmonths VARCHAR(255) NOT NULL,
    expyears VARCHAR(255) NOT NULL
);

CREATE TABLE IF NOT EXISTS Comment(
    id INT PRIMARY KEY AUTO_INCREMENT,
    names VARCHAR(255) NOT NULL,
    emails VARCHAR(255) NOT NULL,
    messages VARCHAR(255) NOT NULL,
    dates VARCHAR(255) NOT NULL,
    timez VARCHAR(255) NOT NULL
);